class circle 
{	
	my_center; 
	my_radius; 

	public int center 
	{
		get 
		{	
			return my_center; 
		}
		set 
		{
			my_center = value; 
		}
	}	
	
	public int radius; 
	{
		get 
		{
			return my_radius; 
		}
		set 
		{
			my_radius = value; 
		}
	}
}

